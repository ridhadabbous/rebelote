let currentLang = localStorage.getItem('rebelo_lang') || 'ar';

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('rebelo_lang', lang);
    document.documentElement.lang = lang === 'ar' ? 'ar' : 'en';
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    const dict = translations[lang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            const tag = el.tagName;
            if (tag === 'TITLE') {
                document.title = dict[key];
            } else if (tag === 'META') {
                el.setAttribute('content', dict[key]);
            } else {
                el.innerHTML = dict[key];
            }
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.setAttribute('placeholder', dict[key]);
        }
    });
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        if (dict['lang_toggle_aria']) {
            toggleBtn.setAttribute('aria-label', dict['lang_toggle_aria']);
        }
        if (dict['lang_toggle_text']) {
            toggleBtn.textContent = dict['lang_toggle_text'];
        }
    }
}

function toggleLanguage() {
    const newLang = currentLang === 'en' ? 'ar' : 'en';
    applyLanguage(newLang);
}

// Wait for DOM
document.addEventListener('DOMContentLoaded', () => {
    applyLanguage(currentLang);

    // Attach toggle to button if it exists
    const btn = document.getElementById('lang-toggle');
    if (btn) {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            toggleLanguage();
        });
    }
});
